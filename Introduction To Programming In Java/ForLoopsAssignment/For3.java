public class For3 {
	public static void main(String[]args){
		int counter = 0;
		for(counter = 0; counter <= 100; counter++){ // this loop counts from 0 to 100
		}
		for(counter = 100; counter>=0; counter--){ // this loop counts from 100 back down to 0	
		}
	}
}

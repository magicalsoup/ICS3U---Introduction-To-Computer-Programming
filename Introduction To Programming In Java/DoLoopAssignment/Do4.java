public class Do4 {
	public static void main(String[]args){
		int index = 1; // declare and set this as 1
		do { // run this part first as long index is smaller than or equal to 10
			System.out.println(index + ". James"); 
			index++; // increment the index by 1
		}
		while(index <=10); // check condition for do loop
	}
}

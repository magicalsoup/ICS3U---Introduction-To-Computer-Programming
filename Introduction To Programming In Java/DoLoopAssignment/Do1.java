public class Do1 {
	public static void main(String[]args){
		int index = 0; // declare and set this as 0
		do { // run this part first as long index is smaller than 10
			System.out.println("James"); 
			index++; // increment the index by 1
		}
		while(index <10); // check condition for do loop
	}
}
